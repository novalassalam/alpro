#include <iostream>
using namespace std;

// while (syarat pengulangan)
// {
//     statement yang dijalankan;
//     statement control:
// }

int main() {
	int a = 12; //kondisi awal
	while(a > 7) { //syarat perulangan ketika a <=10
		cout<<a<<endl;
		// a=a-1;//statement yang diharapkan
		a--;
	}
}